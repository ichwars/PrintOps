export function readinessFindingText(language: string | undefined, code: string, fallbackTitle: string, fallbackCorrection: string) {
  const german = language?.toLowerCase().startsWith('de') ?? false;
  const titles = german ? DE_TITLES : EN_TITLES;
  const corrections = german ? DE_CORRECTIONS : EN_CORRECTIONS;
  return {
    title: titles[code] ?? fallbackTitle,
    correction: corrections[code] ?? fallbackCorrection,
  };
}
